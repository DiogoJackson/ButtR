#' Sum two numbers.
#'
#' This function calculates the sum of two numbers.
#'
#' @param a The first number.
#' @param b The second number.
#' @return The sum of \code{a} and \code{b}.
#' @examples
#' somar(2, 3)
#' # Returns: 5
#'
#' somar(10, -5)
#' # Returns: 5
#'
#' @export
somar <- function(a, b) {
  a + b
}

